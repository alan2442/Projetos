/**l) Elaborar um programa que efetue a leitura de três valores (A, B e C) e apresente como resultado final à
soma dos quadrados dos três valores lidos --> */

var valorA = parseInt(prompt("digite o valor de A "))
var valorB = parseInt(prompt("digite o valor de B "))
var valorC = parseInt(prompt("digite o valor de C "))


var soma = (valorA**2)+(valorB**2)+(valorC**2)


alert("ValorA: "+ valorA)
alert("ValorB "+ valorB)
alert("ValorC: "+ valorC)
alert("A soma dos valores de A, B e C ao quadrado é: "+ soma)