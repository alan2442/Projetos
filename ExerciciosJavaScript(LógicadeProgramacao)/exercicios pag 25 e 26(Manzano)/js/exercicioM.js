/**m) Elaborar um programa que efetue a leitura de três valores (A,B e C) e apresente como resultado final o
quadrado da soma dos três valores lidos. */



var valorA = parseInt(prompt("digite o valor de A:"))
var valorB = parseInt(prompt("digite o valor de B:"))
var valorC = parseInt(prompt("digite o valor de C:"))


var soma =  (valorA+valorB+valorC)**2

alert("Somando A, B e C, e depois deixando o valor ao quadrado o resultado é: "+ soma)