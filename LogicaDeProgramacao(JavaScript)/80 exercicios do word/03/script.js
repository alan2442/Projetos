//Crie um programa que leia a base e a altura de um triângulo e calcule sua área. 

alert("Programa que lê a base e altura de um triângulo e calcula sua área")

var baseDoTriangulo = parseFloat (prompt("Insira a base do seu triangulo em centimetros."))
var alturaDoTriangulo = parseFloat (prompt("Agora, insira a altura do seu triangulo em centimetros."))

var areaDoTriangulo = baseDoTriangulo * alturaDoTriangulo / 2

alert ("A área do seu triangulo é igual a:"+areaDoTriangulo+"cm²")




