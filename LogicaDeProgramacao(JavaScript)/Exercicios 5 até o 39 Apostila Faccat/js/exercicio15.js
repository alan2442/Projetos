/* 15) Ler um valor e escrever se é positivo ou negativo (considere o valor zero como positivo).*/

alert("15) Ler um valor e escrever se é positivo ou negativo (considere o valor zero como positivo).")

var numero = parseInt(prompt("Digite um Número:"))

if (numero < 0) {
    alert("Numero digitado negativo")
} else {
    alert("Numero digitado positivo")
}